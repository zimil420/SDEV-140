# File: myinfo.py
# This script prompts the user to input their name, address, and phone number, and then displays the information.

# Prompt the user to enter their name and store it in the variable strName
strName = input("Enter your name: ")

# Prompt the user to enter their address and store it in the variable strAddress
strAddress = input("Enter your address: ")

# Prompt the user to enter their phone number and store it in the variable strPhone
strPhone = input("Enter your phone number: ")

# Display the name, address, and phone number entered by the user
print("\nHere is the information you entered:")
print("Name:", strName)
print("Address:", strAddress)
print("Phone Number:", strPhone)
